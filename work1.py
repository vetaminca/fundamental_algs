# Проверка, лежит ли точка внутри треугольника (или на его границе),
# с использованием формулы Герона для вычисления площадей.
# Python 3, стандартная библиотека (math).

import math

def dist(x1, y1, x2, y2):
    """
    Евклидово расстояние между двумя точками (x1,y1) и (x2,y2).
    """
    return math.hypot(x2 - x1, y2 - y1)

def heron_area(a, b, c):
    """
    Площадь треугольника по трём сторонам по формуле Герона:
      p = (a + b + c) / 2
      S = sqrt(p * (p - a) * (p - b) * (p - c))
    Для устойчивости при плавающей арифметике, если выражение под корнем
    оказалось немного отрицательным (например, -1e-16), считаем его за 0.
    """
    # p - полупериметр треугольника ABC
    p = 0.5 * (a + b + c)
    # выражение под корнем
    radicand = p * (p - a) * (p - b) * (p - c)
    # защитимся от небольших отрицательных погрешностей
    if radicand < 0 and radicand > -1e-16:
        radicand = 0.0
    if radicand < 0:
        # если всё-таки сильно отрицательное — сторон не может быть треугольник
        return 0.0
    return math.sqrt(radicand)

def point_in_triangle(ax, ay, bx, by, cx, cy, px, py, eps=1e-9):
    """
    Основная функция:
    - Считает площадь большого треугольника ABC.
    - Считает площади трёх треугольников PAB, PBC, PCA.
    - Если сумма площадей PAB+PBC+PCA равна площади ABC с точностью eps,
      то точка P лежит внутри или на границе треугольника.
    Возвращает True (внутри или на границе) или False (снаружи).
    Также обрабатывается вырожденный (дегenerate) треугольник.
    """
    # длины сторон большого треугольника ABC
    a = dist(bx, by, cx, cy)  # сторона opposite A
    b = dist(cx, cy, ax, ay)  # сторона opposite B
    c = dist(ax, ay, bx, by)  # сторона opposite C

    # площадь большого треугольника
    area_ABC = heron_area(a, b, c)

    # если площадь очень близка к нулю — треугольник вырожден
    if area_ABC <= eps:
        # треугольник вырожден: нельзя корректно определить "внутри"
        # В такой ситуации можно дополнительно проверить лежит ли точка
        # на отрезке между двумя точками (если все три коллинеарны) —
        # но в задаче этого обычно не требуется, поэтому считаем: не применимо.
        raise ValueError("Треугольник вырожден (площадь ~ 0). Невозможно определить 'внутри/снаружи'.")

    # площади трёх малых треугольников, образованных точкой P
    # Треугольник PAB:
    ab = dist(ax, ay, bx, by)
    pa = dist(px, py, ax, ay)
    pb = dist(px, py, bx, by)
    area_pab = heron_area(ab, pa, pb)

    # Треугольник PBC:
    bc = dist(bx, by, cx, cy)
    pc = dist(px, py, cx, cy)
    area_pbc = heron_area(bc, pb, pc)

    # Треугольник PCA:
    ca = dist(cx, cy, ax, ay)
    area_pca = heron_area(ca, pc, pa)

    # сумма площадей малых треугольников
    area_sum = area_pab + area_pbc + area_pca

    # сравниваем с учётом погрешности eps
    # если разница меньше eps => внутри или на границе
    return abs(area_sum - area_ABC) <= eps

# Пример 1: точки треугольника и точка (2,2)
A1 = (0.0, 0.0)
B1 = (5.0, 0.0)
C1 = (0.0, 5.0)
P1 = (2.0, 2.0)

# Пример 2: та же тройка вершин, точка (12,12)
P2 = (12.0, 12.0)

# Проверка и вывод результатов
def nice_print_result(A, B, C, P):
    ax, ay = A
    bx, by = B
    cx, cy = C
    px, py = P
    try:
        inside = point_in_triangle(ax, ay, bx, by, cx, cy, px, py)
    except ValueError as e:
        print(f"Ошибка: {e}")
        return
    if inside:
        print(f"Точка ({px:.6g}, {py:.6g}) находится ВНУТРИ или на границе треугольника с вершинами "
              f"({ax:.6g},{ay:.6g}), ({bx:.6g},{by:.6g}), ({cx:.6g},{cy:.6g}).")
    else:
        print(f"Точка ({px:.6g}, {py:.6g}) находится СНАРУЖИ треугольника с вершинами "
              f"({ax:.6g},{ay:.6g}), ({bx:.6g},{by:.6g}), ({cx:.6g},{cy:.6g}).")

# Вывод для двух примеров из задания
nice_print_result(A1, B1, C1, P1)  # ожидаем: внутри
nice_print_result(A1, B1, C1, P2)  # ожидаем: снаружи